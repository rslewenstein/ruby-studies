# Aritméticos
puts 10 + 5
puts 10 / 3
puts 10.0 / 3
puts 2 ** 4 
# No C#, não existe **, só Math.Pow.

# Comparação
puts 7 == 7    # true
puts 7 != 5    # true
puts 5 <=> 7   # -1
# O <=> é chamado de operador espaçonave (spaceship operator) → muito usado em ordenação.

# Lógicos
x = 10
puts x > 5 && x < 20   # true
puts !(x == 10)        # false
